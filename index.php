    <script src="https://unpkg.com/aos@2.3.1/dist/aos.js"></script>
    <script>
        // Initialize Animate On Scroll
        AOS.init({
            once: true, // Animations happen once
            offset: 100, // Trigger offset
            easing: 'ease-out-cubic',
        });

        // 1. Dynamic Navbar: Transparent at top, Blur/White on scroll
        const navbar = document.getElementById('navbar');
        const navContainer = document.getElementById('nav-container');
        
        window.addEventListener('scroll', () => {
            if (window.scrollY > 20) {
                navbar.classList.remove('transparent-nav');
                navbar.classList.add('glass-nav');
                navContainer.classList.remove('h-24');
                navContainer.classList.add('h-20');
            } else {
                navbar.classList.add('transparent-nav');
                navbar.classList.remove('glass-nav');
                navContainer.classList.add('h-24');
                navContainer.classList.remove('h-20');
            }
        });

        // 2. Number Counter Animation
        const counters = document.querySelectorAll('.counter');
        const speed = 200; // The lower the slower

        const animateCounters = () => {
            counters.forEach(counter => {
                const updateCount = () => {
                    const target = +counter.getAttribute('data-target');
                    const count = +counter.innerText;
                    
                    // Lower inc to slow and higher to slow
                    const inc = target / speed;

                    if (count < target) {
                        // Add inc to count and output in counter
                        counter.innerText = Math.ceil(count + inc);
                        setTimeout(updateCount, 20);
                    } else {
                        counter.innerText = target;
                    }
                };
                updateCount();
            });
        }

        // Trigger counter animation when Stats section comes into view
        // We use Intersection Observer for this
        let statsSection = document.querySelector('.counter')?.closest('div.grid');
        if(statsSection) {
            let observer = new IntersectionObserver((entries, observer) => { 
                entries.forEach(entry => {
                    if(entry.isIntersecting){
                        animateCounters();
                        observer.unobserve(entry.target);
                    }
                });
            }, {threshold: 0.5});
            observer.observe(statsSection);
        }

        // 3. Smooth Scroll
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                e.preventDefault();
                document.querySelector(this.getAttribute('href')).scrollIntoView({
                    behavior: 'smooth'
                });
            });
        });

        // 4. About Section Image Slider
        const sliderContainer = document.getElementById('album-container');
        if(sliderContainer) {
            const slides = sliderContainer.querySelectorAll('.slide-item');
            const dots = sliderContainer.querySelectorAll('#slider-dots button');
            let currentSlide = 0;
            let slideInterval;

            const showSlide = (index) => {
                slides.forEach((slide, i) => {
                    if(i === index) {
                        slide.classList.remove('opacity-0', 'scale-110');
                        slide.classList.add('opacity-100', 'scale-100');
                    } else {
                        slide.classList.remove('opacity-100', 'scale-100');
                        slide.classList.add('opacity-0', 'scale-110');
                    }
                });
                
                dots.forEach((dot, i) => {
                    if(i === index) {
                        dot.classList.remove('bg-white/30', 'w-2', 'hover:bg-white');
                        dot.classList.add('bg-primary-500', 'w-8');
                    } else {
                        dot.classList.remove('bg-primary-500', 'w-8');
                        dot.classList.add('bg-white/30', 'w-2', 'hover:bg-white');
                    }
                });
                currentSlide = index;
            };

            const nextSlide = () => {
                let next = (currentSlide + 1) % slides.length;
                showSlide(next);
            };

            // Start loop
            const startSlide = () => {
                slideInterval = setInterval(nextSlide, 4000); // 4 seconds
            };

            const stopSlide = () => {
                clearInterval(slideInterval);
            };

            // Click on dots
            dots.forEach((dot, index) => {
                dot.addEventListener('click', (e) => {
                    e.stopPropagation(); // prevent triggering parent clicks if any
                    stopSlide();
                    showSlide(index);
                    startSlide();
                });
            });

            // Pause on hover
            sliderContainer.addEventListener('mouseenter', stopSlide);
            sliderContainer.addEventListener('mouseleave', startSlide);

            // Init
            startSlide();
        }


    </script>
